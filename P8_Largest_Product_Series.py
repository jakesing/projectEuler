def largest_product_series(largenum, numproduct):
    running_score = 0
    best = ''
    for index in range(len(largenum)-numproduct+1):
        tester = largenum[index:index+numproduct]
        mult = 1
        for i in tester:
            mult *= int(i)
        if mult > running_score:
            running_score = mult
            best = tester

    return running_score, best


